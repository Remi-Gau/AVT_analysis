function filename = ReturnFilename(FileType, SubjectName, HemiSphere, NbLayers, ROI, SurfParam)

    if isempty(NbLayers)
        NbLayers = 6;
    end

    switch lower(FileType)
        case 'hs_run_cdt_layer'
            filename = [ ...
                        SubjectName,  '_features', ...
                        '_hs-', HemiSphere, ...
                        '_nbLayer-', num2str(NbLayers), '.mat' ...
                       ];

        case 'hs_roi_run_cdt_layer'
            filename = [ ...
                        SubjectName, ...
                        '_hs-', HemiSphere, ...
                        '_roi-', ROI, ...
                        '_nbLayer-', num2str(NbLayers), '.mat' ...
                       ];

        case 'hs_roi_run_cdt_s-param'
            filename = [ ...
                        SubjectName, ...
                        '_hs-', HemiSphere, ...
                        '_roi-', ROI, ...
                        '_param-', lower(SurfParam), ...
                        '_nbLayer-', num2str(NbLayers), '.mat' ...
                       ];

    end
end
